
class Budget:

    def __init__(self, budget_name, budget_value, budget_account, budget_balance):
        self.name = budget_name
        self.value = budget_value
        self.account = budget_account
        self.balance = budget_balance
    
    def get_name():
        return self.name

    def get_value():
        return self.value

    def get_account():
        return self.account

    def get_balance():
        return self.balance