
class Budget:

    def __init__(self, budget_name, budget_value, budget_account):
        self.reason = budget_name
        self.value = budget_value
        self.budget_account = budget_account
    
    def get_budget_name():
        return self.budget_name

    def get_budget_value():
        return self.budget_value

    def get_budget_account():
        return self.budget_account