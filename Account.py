class Account:

    def __init__(self, acc_number, acc_name, acc_balance):
        self.acc_number = acc_number
        self.acc_name = acc_name
        self.acc_balance = acc_balance

    
    def get_acc_number():
        return self.acc_number

    def get_acc_name():
        return self.acc_name

    def get_acc_balance():
        return self.acc_balance